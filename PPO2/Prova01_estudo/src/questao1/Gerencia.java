package questao1;

public class Gerencia {
	
	private int codAcao;
	private int qtd;
	private float valor;
	
	
	//get set
	public int getCodAcao() {
		return codAcao;
	}
	public void setCodAcao(int codAcao) {
		this.codAcao = codAcao;
	}
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	
}
